export class User{
    #localStorageKey;
    users;
    fullName;
    email;
    password;
    role;

    constructor(localStorageKey){
        this.#localStorageKey = localStorageKey;
        this.users = undefined;
        this.#loadFromStorage();
    }

    #loadFromStorage(){
        this.users = JSON.parse(localStorage.getItem(this.#localStorageKey)) || [];
    }

    saveToStorage(){
        localStorage.setItem(this.#localStorageKey, JSON.stringify(this.users));
    }

    register(fullName, email, password){
        let autoGeneratedId = this.users.length === 0 ? 1 : Math.max(...this.users.map(u => u.id)) + 1;
        const emailCheck = email.trim().toLowerCase();
        const findDuplicateRegister = this.users.find(u => u.email.toLowerCase() === emailCheck);

        if(findDuplicateRegister){
            alert('Error this email already registered');
            return;
        }

        let role = email === 'andreidejesus@gmail.com' ? 'admin' : 'user';
        const newUserObj = {
            id: autoGeneratedId,
            fullName,
            email,
            password,
            role
        };
        this.users.push(newUserObj);
        this.saveToStorage();

        autoGeneratedId++;
    }

    checkLogin(email, password){
        const userFound = this.users.find(u => u.email === email);
        if (!userFound) {
            return { success: false, error: 'email' };
        }
        if (userFound.password !== password) {
            return { success: false, error: 'password' };
        }
        return { success: true, userExist: userFound }
    }

}